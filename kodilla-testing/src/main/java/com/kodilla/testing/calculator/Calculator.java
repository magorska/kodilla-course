package com.kodilla.testing.calculator;

public class Calculator {

    public double addAToB(double a, double b) {
        return a + b;
    }

    public double getAddAToB() {
        return addAToB;
    }
    public double subtractAToB(double a, double b) {
        return b - a;
    }

    public double getSubtractAToB() {
        return subtractAToB;
    }
}
